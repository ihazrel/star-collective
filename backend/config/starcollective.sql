CREATE TABLE "User" (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL,
    Email VARCHAR(100) UNIQUE,
    PhoneNumber VARCHAR2(20)
);

CREATE TABLE Vendor (
    VendorId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CompanyName VARCHAR2(100),
    Address VARCHAR2(200),
    DateJoin DATE,
    CONSTRAINT vendorid_fk FOREIGN KEY (VendorId) REFERENCES "User"(id)
);

CREATE TABLE Customer (
    CustomerId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MembershipNo VARCHAR2(50),
    DateJoin DATE,
    CONSTRAINT customer_fk FOREIGN KEY (CustomerId) REFERENCES "User"(Id)
);

CREATE TABLE Staff (
    StaffId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Position VARCHAR2(50),
    Salary NUMBER(10,2),
    DateHired DATE,
    ManagedBy NUMBER,
    CONSTRAINT user_fk FOREIGN KEY (StaffId) REFERENCES "User"(Id),
    CONSTRAINT manager_fk FOREIGN KEY (ManagedBy) REFERENCES Staff(StaffId)
);

CREATE TABLE Item (
    ItemId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL,
    Price NUMBER(10,2),
    CurrentStock NUMBER,
    LastUpdateDateTime TIMESTAMP
);


CREATE TABLE Cloth (
    ClothId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ClothSize VARCHAR2(100),
    IsAdultOrChild VARCHAR2(10),
    Sex VARCHAR2(10),
    CONSTRAINT cloth_fk FOREIGN KEY (ClothId) REFERENCES Item(ItemId)
);

CREATE TABLE Sale (
    SaleId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SaleDateTime TIMESTAMP,
    TotalPrice NUMBER(10,2),
    CustomerId NUMBER NOT NULL,
    StaffId NUMBER NOT NULL,
    CONSTRAINT customerid_fk FOREIGN KEY (CustomerId) REFERENCES Customer(CustomerId),
    CONSTRAINT staffid_fk FOREIGN KEY (StaffId) REFERENCES Staff(StaffId)
);



CREATE TABLE SaleDetail (
    SaleDetailId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SaleId NUMBER NOT NULL,
    ItemId NUMBER NOT NULL,
    Quantity NUMBER NOT NULL,
    FinalPrice NUMBER(10,2),
    CONSTRAINT saledetail_fk FOREIGN KEY (SaleId) REFERENCES Sale(SaleId),
    CONSTRAINT detailitem_fk FOREIGN KEY (ItemId) REFERENCES Item(ItemId)
);


CREATE TABLE ItemCategory (
    Id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL
);

CREATE TABLE PurchaseOrder (
    PurchaseOrderId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VendorId NUMBER,
    StaffId NUMBER, 
    OrderDateTime TIMESTAMP,
    InvoiceNo VARCHAR2(50),
    TotalPrice NUMBER(10,2),
    CONSTRAINT vendor_fk FOREIGN KEY (VendorId) REFERENCES Vendor(VendorId),
    CONSTRAINT stafforder_fk FOREIGN KEY (StaffId) REFERENCES Staff(StaffId)
);


CREATE TABLE PurchaseOrderDetail (
    PODetailId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PurchaseOrderId NUMBER, 
    ItemId NUMBER,
    Quantity NUMBER, 
    CONSTRAINT podetail_fk FOREIGN KEY (PurchaseOrderId) REFERENCES PurchaseOrder(PurchaseOrderId),
    CONSTRAINT itemidpd_fk FOREIGN KEY (ItemId) REFERENCES Item(ItemId)
);

CREATE TABLE Cart (
    CartId NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CustomerId NUMBER,
    ItemId NUMBER,
    CONSTRAINT cartcustomer_fk FOREIGN KEY (CustomerId) REFERENCES Customer(CustomerId),
    CONSTRAINT cartitem_fk FOREIGN KEY (ItemId) REFERENCES Item(ItemId)
);

